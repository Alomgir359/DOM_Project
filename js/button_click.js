function btnClick1(){
    var btn = document.getElementById("Completted-btn1");
    btn.disabled = true;
    btn.classList.remove("bg-[#3752FD]")
    btn.classList.add("disablecolor")
    const text=document.getElementById("task").innerText
    const intText=parseInt(text)
    document.getElementById("task").innerText=intText-1
    const text1=document.getElementById("23").innerText
    const intText1=parseInt(text1)
    document.getElementById("23").innerText=intText1+1
    const title=document.getElementById("btn-title1").innerText
    const now = new Date().toLocaleString(); // বর্তমান সময় পাওয়া
    
    const parent = document.getElementsByClassName("history-container")[0]; // প্রথম এলিমেন্ট নেওয়া হলো
    const p = document.createElement("p");
    p.innerText = `You Have Completted The Task ${title} ${now} `;
    p.classList.add("container")
    parent.appendChild(p);

    if(intText===1){
        alert("Board Updated Soccessfully")
        alert("Congrates!! You have Successfylly Done All the task")

    }
    else{
        alert("Board Updated Soccessfully")
    }

}

